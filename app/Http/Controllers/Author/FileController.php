     * Display the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function show($id)
    {
        $file = File::find($id);
        switch (str_replace(' ','',$file->type_file))
        {
            case 'audio':
                return view('author.files.audio.show',['audio' => $file]);
                break;
            case 'photo':
                return view('author.files.photo.show',['photo' => $file]);
                break;
            case 'video':
                return view('author.files.video.show',['video' => $file]);
                break;
        }

    }

